export const getFieldNameByCategory = (category: string) => {
  let fieldName = ''
  switch (category) {
    case 'Audio si tehnologie':
      fieldName = 'audio_and_technology'
      break
    case 'Confort si echipamente optionale':
      fieldName = 'confort_and_extra_options'
      break
    case 'Electronice si sisteme de asistenta':
      fieldName = 'electronics_and_assistance_systems'
      break
    case 'Performanta':
      fieldName = 'performance'
      break
    case 'Siguranta':
      fieldName = 'safety'
      break
  }
  return fieldName
}
